import turtle

#liigtume kohta, et kujund oleks keskel

turtle.penup()
turtle.goto(0, 200)
turtle.pendown()

#hakkame joonistama

turtle.left(120)
turtle.back(400)
turtle.left(60)
turtle.forward(400)
turtle.right(120)
turtle.forward(400)
turtle.right(60)

#üks kolmnurk on olemas, teeme sisemise. Liigume nii, et kujund oleks keskel ja ilus ja kaunis

